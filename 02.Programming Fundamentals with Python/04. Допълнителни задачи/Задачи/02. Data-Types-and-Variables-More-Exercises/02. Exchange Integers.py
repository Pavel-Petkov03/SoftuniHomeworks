# Read two integer numbers and after that exchange their values by using some programming logic.
# Print the variable values before and after the exchange, as shown below:
a = int(input())
b = int(input())
print("Before:")
print(f'a = {a}')
print(f'b = {b}')
print("After:")
y = a
a = b
b = y
print(f'a = {a}')
