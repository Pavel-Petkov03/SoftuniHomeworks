# Трябва да се сметне колко ще струва рождения ден на Людмила ако  напишем в конзолата колко струва залата и ако
# знаем , че торта  20% от наема на залата ,цената на напитките  е 45% по-малко от тази на тортата
# Цената на аниатора е 1/3 от цената на залата
hole = float(input())
cake = 20/100 * hole
drinks = cake - 45/100 * cake
animator = hole / 3
