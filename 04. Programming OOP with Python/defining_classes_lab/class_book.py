# Create a program that reads a positive integer N as input and prints on the console a rhombus with size n:

class Book:
    def __init__(self, name, author, pages):
        self.name = name
        self.author = author
        self.pages = int(pages)


book = Book("My Book", "Me", 200)
print(book.name)
print(book.author)
print(book.pages)

